function builtinByName(name: string) {
  const normalized = name.startsWith("builtins::") ? name.slice("builtins::".length) : name;
  return BUILTIN_FUNCS.find((b) => b.name === normalized);
}

function preprocessByName(name: string) {
  const normalized = name.startsWith("#") ? name : `#${name}`;
  return PREPROCESSOR_ITEMS.find((p) => p.label === normalized);
}

function workspaceRootForGeneratedDecl(): string {
  if (workspaceRoots.length > 0) return workspaceRoots[0];
  if (pythonSysPathRoot) return pythonSysPathRoot;
  return process.cwd();
}

function generateBuiltinDeclFile(builtins: BuiltinFunc[]): void {
  const root = workspaceRootForGeneratedDecl();
  const dir = path.join(root, ".kval");
  const f = path.join(dir, "builtins.kvi");
  fs.mkdirSync(dir, { recursive: true });
  const lines: string[] = [];
  lines.push("// Auto-generated by Kval language server");
  lines.push("namespace builtins {");
  builtinDeclLine.clear();
  for (const b of builtins) {
    builtinDeclLine.set(b.name, lines.length);
    lines.push(`\t${b.returnType} ${b.name}(${b.params.join(", ")});`);
  }
  lines.push("}");
  fs.writeFileSync(f, lines.join("\n") + "\n", "utf8");
  builtinDeclUri = URI.file(f).toString();
}

async function refreshBuiltinSignatures(): Promise<void> {
  if (builtinLoadInFlight) return;
  builtinLoadInFlight = true;
  try {
    const script = path.join(extensionRoot, "scripts", "kval_builtin_signatures.py");
    if (!extensionRoot || !fs.existsSync(script)) return;
    const child = spawn(pythonPath, [script], {
      env: { ...process.env, PYTHONPATH: pythonSysPathRoot || process.cwd() },
      windowsHide: true,
    });
    let out = "";
    let err = "";
    child.stdout?.on("data", (d: Buffer) => {
      out += d.toString("utf8");
    });
    child.stderr?.on("data", (d: Buffer) => {
      err += d.toString("utf8");
    });
    const code: number = await new Promise((resolve) => child.on("close", (c) => resolve(c ?? 1)));
    if (code !== 0 || !out.trim()) {
      if (err.trim()) connection.console.error(`builtin signature load failed: ${err.trim()}`);
      return;
    }
    const parsed = JSON.parse(out) as Array<{
      name: string;
      returnType: string;
      signature: string;
      params: Array<{ name: string; type: string; kind: string; has_default: boolean }>;
    }>;
    BUILTIN_FUNCS = parsed.map((p) => ({
      name: p.name,
      returnType: p.returnType || "object",
      signature: p.signature,
      params: p.params.map((x) =>
        x.kind === "VAR_POSITIONAL" ? `...${x.name}` : `${x.type} ${x.name}`
      ),
      doc: "自动解析于 builtin_namespace",
    }));
    generateBuiltinDeclFile(BUILTIN_FUNCS);
  } catch (e) {
    connection.console.error(`builtin signature load exception: ${String(e)}`);
  } finally {
    builtinLoadInFlight = false;
  }
}

function kvalLibRootsFromSettings(): string[] {
  const out: string[] = [];
  const pushIfDir = (p: string) => {
    if (!p) return;
    if (!fs.existsSync(p)) return;
    try {
      if (fs.statSync(p).isDirectory()) out.push(p);
    } catch {
      // ignore invalid path
    }
  };

  // Typical repo root style: <root>/Kval/Lib
  pushIfDir(path.join(pythonSysPathRoot || "", "Kval", "Lib"));
  // If user points directly to Kval root.
  pushIfDir(path.join(pythonSysPathRoot || "", "Lib"));
  // Fallback from all workspace folders.
  for (const root of workspaceRoots) {
    pushIfDir(path.join(root, "Kval", "Lib"));
    pushIfDir(path.join(root, "Lib"));
  }
  return [...new Set(out.map((p) => path.resolve(p)))];
}

function scanFilesRecursive(root: string, base: string, out: string[], depth = 0): void {
  if (!root || depth > 6) return;
  let ents: fs.Dirent[];
  try {
    ents = fs.readdirSync(root, { withFileTypes: true });
  } catch {
    return;
  }
  for (const e of ents) {
    if (e.name.startsWith(".")) continue;
    const full = path.join(root, e.name);
    if (e.isDirectory()) {
      if (e.name === "__pycache__" || e.name === ".git" || e.name === "node_modules") continue;
      scanFilesRecursive(full, base, out, depth + 1);
    } else if (e.isFile()) {
      const rel = path.relative(base, full).replace(/\\/g, "/");
      out.push(rel);
    }
  }
}

function includePathCompletions(doc: TextDocument, linePrefix: string): CompletionItem[] {
  const out: CompletionItem[] = [];
  const m = linePrefix.match(/^\s*#include\s+([<"])([^>"\n\r]*)$/);
  if (!m) return out;
  const kind = m[1];
  const typed = m[2] ?? "";
  const fileDir = path.dirname(URI.parse(doc.uri).fsPath);
  const libRoots = kvalLibRootsFromSettings();

  type SourceTag = "cwd" | "lib";
  const candidates = new Map<string, SourceTag>();
  const addFrom = (root: string, source: SourceTag) => {
    const acc: string[] = [];
    scanFilesRecursive(root, root, acc);
    for (const r of acc) {
      if (!candidates.has(r)) candidates.set(r, source);
    }
  };

  if (kind === '"') addFrom(fileDir, "cwd");
  for (const root of libRoots) addFrom(root, "lib");

  const closing = kind === "<" ? ">" : '"';
  for (const [rel, source] of candidates) {
    if (typed && !rel.toLowerCase().startsWith(typed.toLowerCase())) continue;
    out.push({
      label: rel,
      kind: CompletionItemKind.File,
      insertText: rel + closing,
      detail: source === "cwd" ? "cwd" : "Kval/Lib",
      sortText: `${source === "cwd" ? "0" : "1"}:${rel.toLowerCase()}`,
    });
  }
  return out.sort(
    (a, b) =>
      String(a.sortText ?? "").localeCompare(String(b.sortText ?? "")) ||
      String(a.label).localeCompare(String(b.label))
  );
}

interface KvalInitSettings {
  extensionRoot?: string;
  pythonPath?: string;
  workspaceRoot?: string;
  pythonSysPathRoot?: string;
  diagnosticsEnabled?: boolean;
  diagnosticsDelayMs?: number;
}

function applySettings(s: KvalInitSettings): void {
  if (s.extensionRoot !== undefined) extensionRoot = s.extensionRoot;
  if (s.pythonPath !== undefined) pythonPath = s.pythonPath;
  if (s.pythonSysPathRoot !== undefined && s.pythonSysPathRoot.trim()) {
    pythonSysPathRoot = s.pythonSysPathRoot.trim();
  } else if (s.workspaceRoot !== undefined) {
    pythonSysPathRoot = s.workspaceRoot.trim();
  }
  if (s.diagnosticsEnabled !== undefined) diagnosticsEnabled = s.diagnosticsEnabled;
  if (s.diagnosticsDelayMs !== undefined) diagnosticsDelayMs = s.diagnosticsDelayMs;
}

function workspaceFolderFs(params: InitializeParams): string {
  const wf = params.workspaceFolders?.[0]?.uri;
  if (!wf) return "";
  return URI.parse(wf).fsPath;
}

function collectRoots(params: InitializeParams): string[] {
  return (params.workspaceFolders ?? []).map((f) => URI.parse(f.uri).fsPath);
}

