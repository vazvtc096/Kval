connection.onRenameRequest((params) => {
  const doc = documents.get(params.textDocument.uri);
  if (!doc) return null;
  const newName = params.newName;
  if (!newName || !/^[A-Za-z_]\w*$/.test(newName)) {
    return null;
  }
  const model = getModel(doc);
  const resolved = resolveSymbolAt(doc, params.position, model);
  const open = openDocTexts();
  const changes: { [uri: string]: TextEdit[] } = {};
  if (resolved) {
    const rk = resolved.kind as RefKind;
    const occ = symbolOccurrenceRanges(
      rk,
      resolved.name,
      resolved.containerClass,
      workspaceRoots,
      open
    );
    for (const [uri, ranges] of occ) {
      const sorted = [...ranges].sort(
        (a, b) =>
          b.start.line - a.start.line ||
          b.start.character - a.start.character ||
          b.end.character - a.end.character
      );
      changes[uri] = sorted.map((r) => TextEdit.replace(r, newName));
    }
    return { changes };
  }
  const { word } = findWordAt(doc.getText(), doc.offsetAt(params.position));
  if (!word) return null;
  const ranges = rangesOfWordPerLine(doc.getText(), word).sort(
    (a, b) => b.start.line - a.start.line || b.start.character - a.start.character
  );
  changes[doc.uri] = ranges.map((r) => TextEdit.replace(r, newName));
  return { changes };
});

