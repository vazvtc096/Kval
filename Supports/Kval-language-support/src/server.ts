documents.onDidChangeContent((change) => {
  const uri = change.document.uri;
  const text = change.document.getText();
  // Immediately update a regex model, then refresh via lexer/token extractor asynchronously.
  docModels.set(uri, buildRegexModel(text));
  scheduleSymbolModel(change.document);
  scheduleValidate(change.document);
});

documents.onDidClose((e) => {
  docModels.delete(e.document.uri);
  const t = debouncers.get(e.document.uri);
  if (t) clearTimeout(t);
  debouncers.delete(e.document.uri);
  const st = symDebouncers.get(e.document.uri);
  if (st) clearTimeout(st);
  symDebouncers.delete(e.document.uri);
  connection.sendDiagnostics({ uri: e.document.uri, diagnostics: [] });
});

function scheduleValidate(doc: TextDocument): void {
  if (doc.uri.endsWith(".kvi")) {
    connection.sendDiagnostics({ uri: doc.uri, diagnostics: [] });
    return;
  }
  if (!diagnosticsEnabled) {
    connection.sendDiagnostics({ uri: doc.uri, diagnostics: [] });
    return;
  }
  const uri = doc.uri;
  const prev = debouncers.get(uri);
  if (prev) clearTimeout(prev);
  debouncers.set(
    uri,
    setTimeout(() => {
      void validateDocument(doc);
    }, diagnosticsDelayMs)
  );
}

