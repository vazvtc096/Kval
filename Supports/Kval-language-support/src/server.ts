function symKindToLsp(k: SymKind): SymbolKind {
  switch (k) {
    case "class":
      return SymbolKind.Class;
    case "function":
      return SymbolKind.Function;
    case "global":
      return SymbolKind.Variable;
    case "field":
      return SymbolKind.Field;
    case "method":
      return SymbolKind.Method;
    default:
      return SymbolKind.Object;
  }
}

