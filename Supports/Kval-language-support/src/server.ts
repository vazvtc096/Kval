function modelFromJson(jsonText: string): KvalModel | null {
  try {
    const parsed = JSON.parse(jsonText) as {
      classes: Array<{
        name: string;
        declKind: "class" | "struct";
        line: number;
        endLine: number;
        startChar: number;
        members: Array<{
          name: string;
          kind: "method" | "field" | "ctor";
          detail: string;
          line: number;
          startChar: number;
        }>;
      }>;
      functions: Array<{
        name: string;
        params: string;
        returnType: string;
        line: number;
        startChar: number;
      }>;
      globals: Array<{ name: string; typeName: string; line: number; startChar: number }>;
      locals?: Array<{
        name: string;
        typeName: string;
        line: number;
        startChar: number;
        scopeStartLine: number;
        scopeEndLine: number;
      }>;
      params?: Array<{
        name: string;
        typeName: string;
        line: number;
        startChar: number;
        scopeStartLine: number;
        scopeEndLine: number;
      }>;
    };
    const classes = new Map<string, any>();
    for (const c of parsed.classes ?? []) {
      classes.set(c.name, {
        name: c.name,
        declKind: c.declKind,
        line: c.line,
        endLine: c.endLine,
        startChar: c.startChar,
        members: (c.members ?? []).map((m) => ({
          name: m.name,
          kind: m.kind,
          detail: m.detail,
          line: m.line,
          startChar: m.startChar,
        })),
      });
    }
    return {
      modelSource: "lexer",
      classes: classes as unknown as Map<string, any>,
      functions: (parsed.functions ?? []).map((f) => ({
        name: f.name,
        params: f.params,
        returnType: f.returnType,
        line: f.line,
        startChar: f.startChar,
      })),
      globals: (parsed.globals ?? []).map((g) => ({
        name: g.name,
        typeName: g.typeName,
        line: g.line,
        startChar: g.startChar,
      })),
      locals: (parsed.locals ?? []).map((v) => ({
        name: v.name,
        typeName: v.typeName,
        line: v.line,
        startChar: v.startChar,
        scopeStartLine: v.scopeStartLine,
        scopeEndLine: v.scopeEndLine,
      })),
      params: (parsed.params ?? []).map((v) => ({
        name: v.name,
        typeName: v.typeName,
        line: v.line,
        startChar: v.startChar,
        scopeStartLine: v.scopeStartLine,
        scopeEndLine: v.scopeEndLine,
      })),
    };
  } catch {
    return null;
  }
}

function scheduleSymbolModel(doc: TextDocument): void {
  const uri = doc.uri;
  const prev = symDebouncers.get(uri);
  if (prev) clearTimeout(prev);
  symDebouncers.set(
    uri,
    setTimeout(
      () => {
        void (async () => {
          const { ok, jsonText } = await runSymbolExtract(doc.getText(), URI.parse(doc.uri).fsPath);
          if (!ok || !jsonText) {
            docModels.set(uri, buildRegexModel(doc.getText()));
            return;
          }
          const m = modelFromJson(jsonText);
          docModels.set(uri, m ?? buildRegexModel(doc.getText()));
          // Lexer model arrives asynchronously; force client to re-request semantic tokens.
          try {
            void connection.languages.semanticTokens.refresh();
          } catch {
            // ignore refresh errors on clients that do not support it
          }
        })();
      },
      Math.max(80, Math.min(600, diagnosticsDelayMs))
    )
  );
}

function parseParserErrorMessage(msg: string): { line: number; character: number } | undefined {
  const m = /at (\d+):(\d+)/.exec(msg);
  if (m) {
    return {
      line: Math.max(0, parseInt(m[1], 10) - 1),
      character: Math.max(0, parseInt(m[2], 10) - 1),
    };
  }
  const m2 = /at (\d+)\b/.exec(msg);
  if (m2) {
    return { line: Math.max(0, parseInt(m2[1], 10) - 1), character: 0 };
  }
  return undefined;
}

